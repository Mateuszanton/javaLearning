package classes.chapterNo3;

public class IfElseIfLadder {
    public static void main(String args[]) {
        int i;
        for (i = 0; i < 7; i++) {
            if (i == 1)
                System.out.println("i = " + i);
            else if (i == 2)
                System.out.println("i = " + i);
            else if (i == 3)
                System.out.println("i = " + i);
            else if (i == 4)
                System.out.println("i = " + i);
            else if (i == 5)
                System.out.println("i = " + i);
            else
                System.out.println("i is not in range 1 to 5");
        }
    }
}
